const (
	screenWidth  = 704
	screenHeight = 480
)

const (
	tileSize = 32
)

var (
	tilesImage *ebiten.Image
)

func init() {
	// Decode an image from the image file's byte slice.
	img, _, err := image.Decode(bytes.NewReader(Tiles_png))
	if err != nil {
		log.Fatal(err)
	}
	tilesImage = ebiten.NewImageFromImage(img)
}

type Coord struct {
	X int
	Y int
}

type Game struct {
	playLocation Coord
	keys         []ebiten.Key
	layers       [][]int
}

func (g *Game) Update() error {

	location := ((g.playLocation.Y * 22) - 1) + g.playLocation.X

	g.layers[1][location] = 0

	if inpututil.IsKeyJustPressed(ebiten.KeyArrowLeft) {
		g.playLocation.X -= 1
		if g.playLocation.X == 0 {
			g.playLocation.X = 1
		}
	} else if inpututil.IsKeyJustPressed(ebiten.KeyArrowRight) {
		g.playLocation.X += 1
		if g.playLocation.X == 23 {
			g.playLocation.X = 22
		}
	} else if inpututil.IsKeyJustPressed(ebiten.KeyArrowDown) {
		g.playLocation.Y += 1
		if g.playLocation.Y == 15 {
			g.playLocation.Y = 14
		}
	} else if inpututil.IsKeyJustPressed(ebiten.KeyArrowUp) {
		g.playLocation.Y -= 1
		if g.playLocation.Y < 0 {
			g.playLocation.Y = 0
		}
	}

	// update player spritee in layers [1]
	location = ((g.playLocation.Y * 22) - 1) + g.playLocation.X
	g.layers[1][location] = 2

	return nil
}

func (g *Game) Draw(screen *ebiten.Image) {
	w := tilesImage.Bounds().Dx()
	tileXCount := w / tileSize

	const xCount = screenWidth / tileSize
	for _, l := range g.layers {
		for i, t := range l {
			op := &ebiten.DrawImageOptions{}
			op.GeoM.Translate(float64((i%xCount)*tileSize), float64((i/xCount)*tileSize))

			sx := (t % tileXCount) * tileSize
			sy := (t / tileXCount) * tileSize
			screen.DrawImage(tilesImage.SubImage(image.Rect(sx, sy, sx+tileSize, sy+tileSize)).(*ebiten.Image), op)
		}
	}

	//ebitenutil.DebugPrint(screen, fmt.Sprintf("TPS: %0.2f", ebiten.ActualTPS()))
	//ebitenutil.DebugPrint(screen, fmt.Sprintf("Keys: %v", g.keys))

	//location := ((g.playLocation.Y * 22) - 1) + g.playLocation.X

	ebitenutil.DebugPrint(screen, fmt.Sprintf("Player location: %v", g.playLocation))
}

func (g *Game) Layout(outsideWidth, outsideHeight int) (int, int) {
	return screenWidth, screenHeight
}

